export async function clearSupabaseData() {
  const { error: error1 } = await supabase.from("roster_cham_cong").delete().neq("id", 0);
  const { error: error2 } = await supabase.from("nhan_vien").delete().neq("id", 0);
  const { error: error3 } = await supabase.from("thang_luong").delete().neq("id", 0);

  if (error1 && !error1.message.includes('relation "roster_cham_cong" does not exist')) {
    throw new Error(`Lỗi xóa bảng roster_cham_cong: ${error1.message}`);
  }
  if (error2 && !error2.message.includes('relation "nhan_vien" does not exist')) {
    throw new Error(`Lỗi xóa bảng nhan_vien: ${error2.message}`);
  }
  if (error3 && !error3.message.includes('relation "thang_luong" does not exist')) {
    throw new Error(`Lỗi xóa bảng thang_luong: ${error3.message}`);
  }
}

export async function syncEmployeesToSupabase(
  employeeData: Record<string, unknown>[],
  onProgress?: (successCount: number, total: number) => void
) {
  const chunkSize = 100;
  let successCount = 0;

  const mappedRows = employeeData.map((e) => {
    const employeeId = String(e.employeeId || e["Mã NV"] || e["ID Number"] || e.employee_id || e.ma_nv || "").trim();
    const fullName = String(e.fullName || e.full_name || e["Name"] || e["Họ tên"] || "").trim();
    const bankAccountNumber = String(e.bankAccountNumber || e.bank_account_number || e["Bank Account"] || e["Bank Account Number"] || "").trim();
    const salaryScale = String(e.salaryScale || e.salary_scale || e["Salary Scale"] || "").trim();
    const business = String(e.business || e["Business"] || "").trim();
    const center = String(e.center || e.l07 || e["L07"] || "").trim();
    const fromVal = String(e.from || e["From"] || "").trim();
    const toVal = String(e.to || e["To"] || "").trim();
    const className = String(e.className || e.class_name || e["Class Name"] || "").trim();
    const noteDays = String(e.noteDays || e.note_days || e["Note"] || "").trim();

    return {
      business,
      center,
      employee_id: employeeId,
      full_name: fullName,
      bank_account_number: bankAccountNumber,
      salary_scale: salaryScale,
      from: fromVal,
      to: toVal,
      class_name: className,
      note_days: noteDays,
      unique_id: employeeId,
      raw_data: e
    };
  }).filter((e) => e.employee_id);

  const total = mappedRows.length;
  if (total === 0) return { successCount: 0, totalRows: 0 };

  for (let i = 0; i < total; i += chunkSize) {
    const chunk = mappedRows.slice(i, i + chunkSize);
    const { error } = await supabase.from("nhan_vien").upsert(chunk, { onConflict: "employee_id" });
    if (error) {
      if (error.message.includes('relation "nhan_vien" does not exist')) {
        throw new Error("Bảng 'nhan_vien' chưa tồn tại trên Supabase. Vui lòng chạy SQL setup trong phần cấu hình Supabase.");
      }
      throw new Error(`Lỗi đồng bộ Nhân viên [${error.code}]: ${error.message}`);
    }
    successCount += chunk.length;
    if (onProgress) onProgress(successCount, total);
  }

  return { successCount, totalRows: total };
}

